            <p>
                Here is some more goodies from the install.
                I didn't want to go for an aftermarket headunit (yet),
                so I ran an loc.
                The loc I used may not be the best,
                but its far better than running speaker level inputs to the rcas,
                or trying to rig up a high level input connector.
                The loc is connected to the rear speakers using wire that I got out of some cat 5e.
                It was connected to the loc using a lineman splice then soldered and heat shrinked.
                It was then connected to the speaker wires using a military splice then soldered
                and insulated with electrical tape.
                After I had finished the install,
                I wondered why I wasn't getting that much bass.
                I later found out that the red striped wires on the loc are the negatives and not the positives.
                So I had to fix that.
                (tip: if you think a phasing issue is killing your bass,
                remove an rca,
                if you get more bass after that,
                then check the polarities on your loc to speaker connections.
                It can also be done with a fader,
                but not everyone has one)
                The rcas that I used,
                while not that important did recive some chatter back in the day,
                are stinger dream series triple shielded balanced ofc interconnects.
                I got them in a box of cables from someone.
            </p>
