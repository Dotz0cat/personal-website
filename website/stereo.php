            This all started when I found the amplifier that I am using (dual xpe2700) at a yard sale.
            The guy was selling it for $20,
            I offered him $5 and he took it.
            It worked.
            I then converted a computer / home theater subwoofer
            to something that can be used with a car amplifier.
            I installed it,
            and ran everything myself.
            I ran it for a while,
            then set the gain up right with an o-scope.
            Then finally after some more time changed the clip connector I was using to a proper terminal cup.
