                Next up at the front,
                I have my main power connection.
                It's 10 awg TTHN.
                All loomed for protection.
                It's also fused with a 30 amp ANL fuse.
                Some people may have an issue with the fact that I used 10 gauge TTHN.
                For a system that "does" 150 Watts,
                that will never see more than half volume,
                I think 10 gauge is fine.
                For the TTHN part,
                it was a little stiff while running it,
                but not troublesome.
                The insulation is tough.
                Since I used stranded 10 gauge,
                it is way more flexible than the solid counterpart. 
