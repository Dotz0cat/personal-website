            <p>
                Next the subwoofer is the subwoofer from a klipsch promedia gmx-d 5.1 set.
                I had picked it up for free from an e-waste place.
                I didn't get the control pod that is required to run it.
                Since I didn't want to hack at it too much to get it to work without the control pod,
                I just had it collecting dust in storage.
                So after I picked up the amplifier,
                I decided why not just use it in my car.
                So I removed the internal amplifier from it and desoldered the subwoofer.
                I later cut a piece of plywood to cover up the back of it.
                I then used a small clip connector as the connector for it.
                The clips on it wasn't even crimped right.
                I also put in a bit of fiberglass for damping.
                I later found some proper terminal cups,
                but I had to wait as I didn't have the right size of a hole saw.
